/* =============================================
   CLICKABLE APP ICONS WITH EFFECTS
   ============================================= */
function setupAppIcons() {
  const icons = $$('.app-icon');
  const appNames = ['Maps', 'Mail', 'Social', 'Compass', 'Calendar', 'Music', 'Phone', 'Alerts'];
  const colors = ['#3ddc84','#4285f4','#ea4335','#fbbc04','#34a853','#7c4dff','#00bcd4','#ff6d00'];

  icons.forEach((icon, i) => {
    // Add badge element
    const badge = document.createElement('div');
    badge.className = 'app-badge';
    badge.textContent = '!';
    icon.appendChild(badge);

    icon.addEventListener('click', (e) => {
      // 1. Ripple from click point
      const rect = icon.getBoundingClientRect();
      const size = Math.max(rect.width, rect.height) * 2.4;
      const ripple = document.createElement('span');
      ripple.className = 'app-ripple';
      ripple.style.cssText = `width:${size}px;height:${size}px;left:${e.clientX-rect.left-size/2}px;top:${e.clientY-rect.top-size/2}px;`;
      icon.appendChild(ripple);
      ripple.addEventListener('animationend', () => ripple.remove());

      // 2. Launch bounce
      icon.classList.remove('launching');
      void icon.offsetWidth; // reflow
      icon.classList.add('launching');
      icon.addEventListener('animationend', () => icon.classList.remove('launching'), { once: true });

      // 3. Brief notification badge
      icon.classList.add('has-badge');
      setTimeout(() => icon.classList.remove('has-badge'), 1200);

      // 4. Screen flash (subtle glow pulse)
      const screen = icon.closest('.phone-screen');
      if (screen) {
        screen.style.setProperty('--flash-color', colors[i] || '#3ddc84');
        screen.classList.add('screen-flash');
        setTimeout(() => screen.classList.remove('screen-flash'), 400);
      }
    });
  });
}

/* =============================================
   GENERIC COUNT-UP (payout / metric / trust numbers)
   ============================================= */
function setupGenericCounters() {
  if (prefersReducedMotion()) return;
  const targets = $$('.payout-card strong, .top-card strong, .trust-row strong');
  if (!targets.length) return;

  const animate = (el) => {
    const raw = el.dataset.countRaw || el.textContent.trim();
    el.dataset.countRaw = raw;
    const m = raw.match(/^(\D*)([\d.,]+)(.*)$/s);
    if (!m) return; // non-numeric label (e.g. "USDT", "Private") — leave as is
    const prefix = m[1];
    const numStr = m[2].replace(/,/g, '');
    const suffix = m[3];
    const target = parseFloat(numStr);
    if (isNaN(target)) return;
    const decimals = (numStr.split('.')[1] || '').length;
    const duration = 1500;
    const start = performance.now();
    const step = (now) => {
      const p = Math.min((now - start) / duration, 1);
      const eased = 1 - Math.pow(1 - p, 3);
      el.textContent = prefix + (eased * target).toFixed(decimals) + suffix;
      if (p < 1) requestAnimationFrame(step);
      else el.textContent = raw;
    };
    requestAnimationFrame(step);
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        animate(entry.target);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.6 });

  targets.forEach((el) => observer.observe(el));
}

/* =============================================
   BACK TO TOP
   ============================================= */
function setupBackToTop() {
  const btn = document.createElement('button');
  btn.type = 'button';
  btn.className = 'back-to-top';
  btn.setAttribute('aria-label', 'Back to top');
  btn.innerHTML = '<svg viewBox="0 0 24 24" aria-hidden="true"><path d="M12 19V5"></path><path d="m5 12 7-7 7 7"></path></svg>';
  document.body.appendChild(btn);

  btn.addEventListener('click', () => {
    window.scrollTo({ top: 0, behavior: prefersReducedMotion() ? 'auto' : 'smooth' });
  });

  let ticking = false;
  const update = () => {
    btn.classList.toggle('is-visible', window.scrollY > 700);
    ticking = false;
  };
  window.addEventListener('scroll', () => {
    if (!ticking) { requestAnimationFrame(update); ticking = true; }
  }, { passive: true });
  update();
}

