/* =============================================
   PARTICLE CANVAS — Android green/blue palette
   ============================================= */
function setupParticleCanvas() {
  if (prefersReducedMotion()) return;
  const canvas = document.getElementById('particle-canvas');
  if (!canvas) return;

  const ctx = canvas.getContext('2d', { alpha: true });
  let W, H, particles;

  const COUNT = 28;
  const MAX_DIST = 80;
  const MAX_DIST_SQ = MAX_DIST * MAX_DIST;
  const COLORS = [
    'rgba(61, 220, 132,',   // Android green
    'rgba(66, 133, 244,',   // Google blue
    'rgba(0, 188, 212,',    // Teal
    'rgba(124, 77, 255,'    // Violet accent
  ];

  const TARGET_MS = 1000 / 30;
  let lastTime = 0;

  function resize() {
    W = canvas.width = window.innerWidth;
    H = canvas.height = window.innerHeight;
  }

  function createParticle() {
    const color = COLORS[Math.floor(Math.random() * COLORS.length)];
    return {
      x: Math.random() * W, y: Math.random() * H,
      vx: (Math.random() - 0.5) * 0.3,
      vy: (Math.random() - 0.5) * 0.3,
      r: Math.random() * 1.6 + 0.5,
      alpha: Math.random() * 0.45 + 0.15,
      color
    };
  }

  function init() {
    resize();
    particles = Array.from({ length: COUNT }, createParticle);
  }

  function tick(now) {
    requestAnimationFrame(tick);
    if (now - lastTime < TARGET_MS) return;
    lastTime = now;

    ctx.clearRect(0, 0, W, H);

    for (let i = 0; i < COUNT; i++) {
      const p = particles[i];
      p.x += p.vx; p.y += p.vy;
      if (p.x < -10) p.x = W + 10;
      else if (p.x > W + 10) p.x = -10;
      if (p.y < -10) p.y = H + 10;
      else if (p.y > H + 10) p.y = -10;

      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = p.color + p.alpha + ')';
      ctx.fill();

      for (let j = i + 1; j < COUNT; j++) {
        const q = particles[j];
        const dx = p.x - q.x, dy = p.y - q.y;
        const distSq = dx * dx + dy * dy;
        if (distSq < MAX_DIST_SQ) {
          const lineAlpha = (1 - distSq / MAX_DIST_SQ) * 0.14;
          ctx.beginPath();
          ctx.moveTo(p.x, p.y);
          ctx.lineTo(q.x, q.y);
          ctx.strokeStyle = p.color + lineAlpha + ')';
          ctx.lineWidth = 0.6;
          ctx.stroke();
        }
      }
    }
  }

  let resizeTimer;
  window.addEventListener('resize', () => {
    clearTimeout(resizeTimer);
    resizeTimer = setTimeout(() => { resize(); }, 200);
  }, { passive: true });

  init();
  requestAnimationFrame(tick);
}

