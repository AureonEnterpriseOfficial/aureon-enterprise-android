/* =============================================
   CONTACT FORM
   ============================================= */
function setupContactForm() {
  const FORM_ENDPOINT = "https://script.google.com/macros/s/AKfycbz8QlS021hCg82SzCiAims0-AfNii0f1qgQTExtz4SEJ_nXFx6Arb-Y3iX0pjhkMhyWnA/exec";

  const form = $("[data-contact-form]");
  if (!form) return;

  const submitBtn = form.querySelector('[type="submit"]');
  const btnLabel = submitBtn ? submitBtn.querySelector('span') || submitBtn : null;
  let originalLabel = btnLabel ? btnLabel.textContent : "Send Message";

  let feedback = form.querySelector('.form-feedback');
  if (!feedback) {
    feedback = document.createElement('div');
    feedback.className = 'form-feedback';
    form.appendChild(feedback);
  }

  form.addEventListener("submit", async (event) => {
    event.preventDefault();

    const serviceInput = form.querySelector('input[name="service"]');
    if (serviceInput && !serviceInput.value) {
      const wrapper = form.querySelector('[data-custom-select]');
      const btn = wrapper?.querySelector('.custom-select-btn');
      if (btn) {
        btn.focus();
        wrapper.classList.add('select-error');
        setTimeout(() => wrapper.classList.remove('select-error'), 800);
      }
      return;
    }

    if (submitBtn) {
      submitBtn.disabled = true;
      originalLabel = btnLabel ? btnLabel.textContent : submitBtn.textContent;
      if (btnLabel) btnLabel.textContent = "Sending…";
      else submitBtn.textContent = "Sending…";
    }
    feedback.className = 'form-feedback';
    feedback.textContent = '';

    try {
      const data = new FormData(form);
      const payload = {
        name: data.get("name") || "",
        contact: data.get("contact") || "",
        service: data.get("service") || "",
        message: data.get("message") || ""
      };

      const response = await fetch(FORM_ENDPOINT, {
        method: "POST",
        headers: { "Content-Type": "text/plain" },
        body: JSON.stringify(payload)
      });

      const text = await response.text();
      let result = { success: false };
      try { result = JSON.parse(text); } catch { /* not JSON */ }

      if (result.success === "true" || result.success === true || response.ok) {
        feedback.className = 'form-feedback success';
        feedback.textContent = "✓ Message sent! We'll reply within hours.";
        form.reset();
        const valEl = form.querySelector('.custom-select-val');
        const wrapper = form.querySelector('[data-custom-select]');
        if (valEl) valEl.textContent = translations[lang]?.form_service_ph || "Choose an option…";
        if (wrapper) wrapper.classList.remove('has-value');
      } else {
        throw new Error("Server error");
      }
    } catch {
      feedback.className = 'form-feedback error';
      feedback.textContent = "Something went wrong. Please try again or contact us directly.";
    } finally {
      if (submitBtn) {
        submitBtn.disabled = false;
        if (btnLabel) btnLabel.textContent = originalLabel;
        else submitBtn.textContent = originalLabel;
      }
    }
  });
}

