/* =============================================
   COUNTER ANIMATION
   ============================================= */
function setupCounters() {
  const statsPanel = $('.stats-panel');
  if (!statsPanel) return;
  const statEls = $$('.stats-panel strong');
  const parseNumber = (str) => { const m = str.match(/[\d.]+/); return m ? parseFloat(m[0]) : null; };
  const originals = statEls.map(el => el.textContent.trim());

  const animateCounter = (el, target, suffix, duration = 1800) => {
    const start = performance.now();
    const step = (now) => {
      const progress = Math.min((now - start) / duration, 1);
      const eased = 1 - Math.pow(1 - progress, 3);
      el.textContent = Math.round(eased * target) + suffix;
      if (progress < 1) requestAnimationFrame(step);
    };
    requestAnimationFrame(step);
  };

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          statEls.forEach((el, i) => {
            const orig = originals[i];
            const num = parseNumber(orig);
            if (num === null) return;
            animateCounter(el, num, orig.replace(/[\d.]+/, '').trim());
          });
          observer.disconnect();
        }
      });
    },
    { threshold: 0.5 }
  );

  observer.observe(statsPanel);
}

