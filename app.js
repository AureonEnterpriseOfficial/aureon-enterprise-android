function prefersReducedMotion() {
  return window.matchMedia("(prefers-reduced-motion: reduce)").matches;
}

function setupCursor() {
  if (!window.matchMedia("(pointer: fine)").matches || prefersReducedMotion()) return;

  const dot = $("[data-cursor-dot]");
  const halo = $("[data-cursor-halo]");
  if (!dot || !halo) return;

  document.body.classList.add("cursor-enabled");

  let raf = 0, x = 0, y = 0, lastMx = -1, lastMy = -1;
  const W = () => window.innerWidth;
  const H = () => window.innerHeight;

  const applyPosition = () => {
    const tx = x.toFixed(1);
    const ty = y.toFixed(1);
    dot.style.transform = `translate3d(${tx}px,${ty}px,0) translate(-50%,-50%)`;
    halo.style.transform = `translate3d(${tx}px,${ty}px,0) translate(-50%,-50%)`;
    const mx = (x / W()) * 100;
    const my = (y / H()) * 100;
    if (Math.abs(mx - lastMx) > 1 || Math.abs(my - lastMy) > 1) {
      document.documentElement.style.setProperty("--mx", mx.toFixed(1) + "%");
      document.documentElement.style.setProperty("--my", my.toFixed(1) + "%");
      lastMx = mx; lastMy = my;
    }
    raf = 0;
  };

  const move = (event) => {
    x = event.clientX; y = event.clientY;
    document.body.classList.add("cursor-ready");
    if (!raf) raf = requestAnimationFrame(applyPosition);
  };

  window.addEventListener("mousemove", move, { passive: true });
  window.addEventListener("mouseleave", () => document.body.classList.remove("cursor-ready"));
  window.addEventListener("mouseenter", () => document.body.classList.add("cursor-ready"));

  const INTERACTIVE = "a, button, input, select, textarea, .service-card, .why-card, .step-card, .payout-card, .offer-board";

  document.addEventListener("mouseover", (event) => {
    if (event.target.closest(INTERACTIVE)) document.body.classList.add("cursor-active");
  });

  document.addEventListener("mouseout", (event) => {
    if (event.target.closest(INTERACTIVE)) document.body.classList.remove("cursor-active");
  });
}

